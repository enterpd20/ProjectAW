/*
캐릭터가 가져야하는 스탯
체력 - 여기서 씀
이동속도 - 여기서 씀
공격력 - BulletControl에서 장비 데이터 받아와서 씀
재장전 - BulletControl에서 장비 데이터 받아와서 씀
